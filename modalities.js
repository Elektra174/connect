/**
 * –í—Å–ø–æ–º–æ–≥–∞—Ç–µ–ª—å–Ω—ã–π –∫–ª–∞—Å—Å ModalityEngine –¥–ª—è —É–ø—Ä–∞–≤–ª–µ–Ω–∏—è –ª–æ–≥–∏–∫–æ–π –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–µ–π.
 * –û–±–µ—Å–ø–µ—á–∏–≤–∞–µ—Ç —Å–≤—è–∑—å –º–µ–∂–¥—É –¥–∞–Ω–Ω—ã–º–∏ –∏ —Å–µ—Ä–≤–µ—Ä–Ω–æ–π –ª–æ–≥–∏–∫–æ–π.
 */
class ModalityEngine {
    
    /**
     * –ü–æ–ª—É—á–∏—Ç—å –ø–æ–ª–Ω—É—é –∏–Ω—Ñ–æ—Ä–º–∞—Ü–∏—é –æ –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–∏ –ø–æ –µ—ë –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä—É.
     * @param {string} id - –ö–ª—é—á –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–∏ (mpt, cbt, gestalt –∏ —Ç.–¥.)
     * @returns {object} - –û–±—ä–µ–∫—Ç –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–∏ –∏–ª–∏ –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç—å –ø–æ —É–º–æ–ª—á–∞–Ω–∏—é (MPT).
     */
    static getById(id) {
        return MODALITIES[id] || MODALITIES.mpt;
    }

    /**
     * –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç –º–∞—Å—Å–∏–≤ –≤—Å–µ—Ö –¥–æ—Å—Ç—É–ø–Ω—ã—Ö –∏–¥–µ–Ω—Ç–∏—Ñ–∏–∫–∞—Ç–æ—Ä–æ–≤ –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–µ–π.
     * @returns {string[]}
     */
    static getAllIds() {
        return Object.keys(MODALITIES);
    }

    /**
     * –§–æ—Ä–º–∏—Ä—É–µ—Ç –¥–∏–Ω–∞–º–∏—á–µ—Å–∫—É—é –∏–Ω—Å—Ç—Ä—É–∫—Ü–∏—é –¥–ª—è –ø—Ä–æ–º–ø—Ç–∞ –°—É–ø–µ—Ä–≤–∏–∑–æ—Ä–∞.
     * @param {string} id - –ö–ª—é—á –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–∏.
     * @returns {string} - –¢–µ–∫—Å—Ç –¥–ª—è —Å–∏—Å—Ç–µ–º–Ω–æ–≥–æ –ø—Ä–æ–º–ø—Ç–∞.
     */
    static getSupervisorInstruction(id) {
        const mod = this.getById(id);
        const kpis = mod.supervisor_kpis.join(' ');
        const rules = mod.golden_rules.join(' ');
        return `–í –º–µ—Ç–æ–¥–µ ${mod.name} —Ñ–æ–∫—É—Å –Ω–∞ —Å–ª–µ–¥—É—é—â–µ–º: ${kpis}. –ü–æ–º–Ω–∏ –ø—Ä–æ –∑–æ–ª–æ—Ç—ã–µ –ø—Ä–∞–≤–∏–ª–∞: ${rules}`;
    }

    /**
     * –ü—ã—Ç–∞–µ—Ç—Å—è –æ–ø—Ä–µ–¥–µ–ª–∏—Ç—å —Ç–µ–∫—É—â–∏–π —ç—Ç–∞–ø —Å–µ—Å—Å–∏–∏ –Ω–∞ –æ—Å–Ω–æ–≤–µ –¥–ª–∏–Ω—ã –∏—Å—Ç–æ—Ä–∏–∏ –¥–∏–∞–ª–æ–≥–∞.
     * @param {string} id - –ö–ª—é—á –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–∏.
     * @param {number} historyLength - –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–æ–±—â–µ–Ω–∏–π –≤ —á–∞—Ç–µ.
     * @returns {object} - –û–±—ä–µ–∫—Ç —ç—Ç–∞–ø–∞ —Å–µ—Å—Å–∏–∏.
     */
    static guessCurrentStage(id, historyLength) {
        const mod = this.getById(id);
        const stageCount = mod.stages.length;
        
        // –≠–≤—Ä–∏—Å—Ç–∏—á–µ—Å–∫–∞—è –ª–æ–≥–∏–∫–∞ —Ä–∞—Å–ø—Ä–µ–¥–µ–ª–µ–Ω–∏—è —ç—Ç–∞–ø–æ–≤ –ø–æ –¥–ª–∏–Ω–µ –¥–∏–∞–ª–æ–≥–∞
        if (historyLength < 4) return mod.stages[0];
        if (historyLength < 10) return mod.stages[1];
        if (historyLength < 20) return mod.stages[2];
        if (historyLength < 30) return mod.stages[3] || mod.stages[stageCount - 1];
        
        return mod.stages[stageCount - 1];
    }

    /**
     * –í–æ–∑–≤—Ä–∞—â–∞–µ—Ç —Å–ø–µ—Ü–∏—Ñ–∏—á–µ—Å–∫–∏–µ –≤–æ–ø—Ä–æ—Å—ã –¥–ª—è —Ç–µ–∫—É—â–µ–≥–æ —ç—Ç–∞–ø–∞.
     * @param {string} id - –ö–ª—é—á –º–æ–¥–∞–ª—å–Ω–æ—Å—Ç–∏.
     * @param {number} historyLength - –ö–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–æ–±—â–µ–Ω–∏–π.
     * @returns {string[]} - –ú–∞—Å—Å–∏–≤ –≤–æ–ø—Ä–æ—Å–æ–≤.
     */
    static getStageQuestions(id, historyLength) {
        const stage = this.guessCurrentStage(id, historyLength);
        return stage.questions || [];
    }
}

