            "–ß—ë—Ç–∫–æ—Å—Ç—å —Ç–µ—Ä–∞–ø–µ–≤—Ç–∏—á–µ—Å–∫–æ–≥–æ –∫–æ–Ω—Ç—Ä–∞–∫—Ç–∞.",
            "–ê–Ω–∞–ª–∏–∑ —Ä–æ–¥–∏—Ç–µ–ª—å—Å–∫–∏—Ö –ø—Ä–µ–¥–ø–∏—Å–∞–Ω–∏–π (–∏–Ω—ä–µ–∫—Ü–∏–π)."
