            "–õ—é–¥–∏ —Ä–æ–∂–¥–∞—é—Ç—Å—è –ø—Ä–∏–Ω—Ü–∞–º–∏, –∞ —Å—Ü–µ–Ω–∞—Ä–∏–∏ –¥–µ–ª–∞—é—Ç –∏—Ö –ª—è–≥—É—à–∫–∞–º–∏.",
            "–ö–∞–∂–¥—ã–π —á–µ–ª–æ–≤–µ–∫ —Å–ø–æ—Å–æ–±–µ–Ω –¥—É–º–∞—Ç—å –∏ –ø—Ä–∏–Ω–∏–º–∞—Ç—å –Ω–æ–≤—ã–µ —Ä–µ—à–µ–Ω–∏—è."
